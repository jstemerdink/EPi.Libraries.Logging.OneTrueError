namespace EPi.Libraries.Logging.OneTrueError.Configuration
{
    using System;
    using System.Reflection;

    using Newtonsoft.Json;
    using Newtonsoft.Json.Serialization;

    /// <summary>
    /// Special JsonConvert resolver that prevents exceptions.  See http://stackoverflow.com/questions/20962316/ignoring-class-members-that-throw-exceptions-when-serializing-to-json
    /// </summary>
    public class ErrorContractResolver : DefaultContractResolver
    {
        /// <summary>
        /// Check if properties can be serialized.
        /// </summary>
        /// <param name="member">The member to create a <see cref="T:Newtonsoft.Json.Serialization.JsonProperty" /> for.</param>
        /// <param name="memberSerialization">The member's parent <see cref="T:Newtonsoft.Json.MemberSerialization" />.</param>
        /// <returns>The <see cref="JsonProperty" /></returns>
        protected override JsonProperty CreateProperty(MemberInfo member, MemberSerialization memberSerialization)
        {
            JsonProperty property = base.CreateProperty(member, memberSerialization);

            property.ShouldSerialize = instance =>
            {
                try
                {
                    PropertyInfo prop = (PropertyInfo)member;
                    if (prop.CanRead)
                    {
                        prop.GetValue(instance, null);
                        return true;
                    }
                }
                catch (Exception)
                {
                }
                return false;
            };

            return property;
        }
    }
}